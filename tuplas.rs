fn main(){
    // Criando/declarando uma tupla
    let tupla_a = ('B', 9i32, true);
    // Indice da tupla 0 = Valor => B, tipo => char
    // Indice da tupla 1 = Valor => 9, tipo => inteiro 32 bits
    // Indice da tupla 2 = Valor => true, tipo => booleano
    // Leitura dos valores:
    println!("indice 0 {}", tupla_a.0);//Lendo indice 0
    println!("indice 1 {}", tupla_a.1);//Lendo indice 1
    println!("indice 2 {}", tupla_a.2);//Lendo indice 2
    // indice 0 B
    // indice 1 9
    // indice 2 true
}
